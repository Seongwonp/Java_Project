package ch_06_array;

public class My11_01 {
    public static void main(String[] args) {
        //swap
        int num1 =10;
        int num2 =20;
        int swap = num1;
        num1 = num2;
        num2 = swap;
        System.out.println(num1 + " ");
        System.out.println(num2 + " ");
        //렌덤
        System.out.println((int)(Math.random() * 10));

    }
}
